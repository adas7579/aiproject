public int gcd(int x,int y)
    {
        for(int i=Math.min(x, y);i>=1;i--)
        {
            if(x%i==0 && y%i==0)
                return i;
        }
        return 1;
    }
boolean Prime(int a)
{
int j=0;
for(int i=1;i<=a;i++)
{
    if(a%i==0)
        j++;
}
if(j==2)return true;
else return false;
}
