public int gcd(int x,int y)
    {
        if(x>y)
            return gcd(x-y,y);
        else if(y>x)
            return gcd(x,y-x);
        else
            return x;
    }
boolean Prime(int a)
{
int j=0;
for(int i=1;i<=a;i++)
{
    if(a%i==0)
        j++;
}
if(j==2)return true;
else return false;
}
